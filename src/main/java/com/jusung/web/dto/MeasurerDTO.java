package com.jusung.web.dto;

public class MeasurerDTO {
	private String companyNo;
	private String measurerNo;
	private String measurerName;
	private String length;
	private String weight;
	private String baseAngle;
	public String getCompanyNo() {
		return companyNo;
	}
	public void setCompanyNo(String companyNo) {
		this.companyNo = companyNo;
	}
	public String getMeasurerNo() {
		return measurerNo;
	}
	public void setMeasurerNo(String measurerNo) {
		this.measurerNo = measurerNo;
	}
	public String getMeasurerName() {
		return measurerName;
	}
	public void setMeasurerName(String measurerName) {
		this.measurerName = measurerName;
	}
	public String getLength() {
		return length;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getBaseAngle() {
		return baseAngle;
	}
	public void setBaseAngle(String baseAngle) {
		this.baseAngle = baseAngle;
	} 
	
}
