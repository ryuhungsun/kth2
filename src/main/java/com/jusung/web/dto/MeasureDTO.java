package com.jusung.web.dto;

public class MeasureDTO {
	private String measurerNo;
	private String measureDate;
	private String baseAngle;
	private String position1;
	private String position2;
	private String angle;
	public String getMeasurerNo() {
		return measurerNo;
	}
	public void setMeasurerNo(String measurerNo) {
		this.measurerNo = measurerNo;
	}
	public String getMeasureDate() {
		return measureDate;
	}
	public void setMeasureDate(String measureDate) {
		this.measureDate = measureDate;
	}
	public String getBaseAngle() {
		return baseAngle;
	}
	public void setBaseAngle(String baseAngle) {
		this.baseAngle = baseAngle;
	}
	public String getPosition1() {
		return position1;
	}
	public void setPosition1(String position1) {
		this.position1 = position1;
	}
	public String getPosition2() {
		return position2;
	}
	public void setPosition2(String position2) {
		this.position2 = position2;
	}
	public String getAngle() {
		return angle;
	}
	public void setAngle(String angle) {
		this.angle = angle;
	}
	
}
